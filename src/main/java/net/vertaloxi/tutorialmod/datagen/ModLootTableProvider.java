public class ModLootTableProvider extends FabricBlockLootTableProvider {
    public ModLootTableProvider(FabricDataOutput dataOutput, CompletableFuture<RegistryWrapper.WrapperLookup> registryLookup) {
        super(dataOutput, registryLookup);
    }

    @Override
    public void generate() {
        addDrop(ModBlocks.LEGENDARY_DIAMOND_BLOCK);
        addDrop(ModBlocks.MAGIC_BLOCK);

        addDrop(ModBlocks.LEGENDARY_DIAMOND_ORE, oreDrops(ModBlocks.LEGENDARY_DIAMOND_ORE, ModItems.LEGENDARY_DIAMOND));
        addDrop(ModBlocks.SHY_GOLDEN_APPLE_BLOCK, drops(ModBlocks.SHY_GOLDEN_APPLE_BLOCK, ModItems.SHY_GOLDEN_APPLE, new ConstantLootNumberProvider(4)));
    }

}
